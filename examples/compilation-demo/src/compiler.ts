function compileToBabylon(source: string): string {
  return `// Generated by HoloScript Compiler v3.42.0
// Source: ${source.split('\n')[0]}
import { Scene, Engine, Vector3, MeshBuilder, StandardMaterial, Color3, DirectionalLight, PointLight, HemisphericLight } from '@babylonjs/core';
import { HoloScriptRuntime } from '@holoscript/runtime-babylon';

export class DemoScene {
    private scene: Scene;
    private runtime: HoloScriptRuntime;

    constructor(private engine: Engine) {
        this.scene = new Scene(engine);
        this.runtime = new HoloScriptRuntime(this.scene);
    }

    async initialize(): Promise<void> {
        await this.buildScene();

        // Render loop
        this.engine.runRenderLoop(() => {
            this.runtime.update(this.engine.getDeltaTime() / 1000);
            this.scene.render();
        });
    }

    private async buildScene(): Promise<void> {
        // Magic Cube - Spinning with emissive glow
        const magicCube = MeshBuilder.CreateBox("MagicCube", { size: 1 }, this.scene);
        magicCube.position = new Vector3(0, 2, 0);

        const cubeMaterial = new StandardMaterial("MagicCubeMaterial", this.scene);
        cubeMaterial.diffuseColor = this.hexToColor("#ff00ff");
        cubeMaterial.emissiveColor = this.hexToColor("#ff00ff").scale(0.5);
        cubeMaterial.specularPower = 30;
        cubeMaterial.metallic = 0.7;
        cubeMaterial.roughness = 0.3;
        magicCube.material = cubeMaterial;

        // Apply traits
        this.runtime.applyTrait(magicCube, '@spatial');
        this.runtime.applyTrait(magicCube, '@networked');
        this.runtime.applyTrait(magicCube, '@rotate', { speed: 1 });
        this.runtime.applyTrait(magicCube, '@emissive');

        // Floating Sphere with pulse
        const floatingSphere = MeshBuilder.CreateSphere("FloatingSphere", { diameter: 1 }, this.scene);
        floatingSphere.position = new Vector3(3, 2, 0);

        const sphereMaterial = new StandardMaterial("SphereMaterial", this.scene);
        sphereMaterial.diffuseColor = this.hexToColor("#00ffff");
        sphereMaterial.roughness = 0.5;
        floatingSphere.material = sphereMaterial;

        this.runtime.applyTrait(floatingSphere, '@spatial');
        this.runtime.applyTrait(floatingSphere, '@float');
        this.runtime.applyTrait(floatingSphere, '@pulse');

        // Portal
        const portal = MeshBuilder.CreateBox("ToAnotherZone", { size: 1 }, this.scene);
        portal.position = new Vector3(0, 1, -5);
        portal.metadata = { destination: "main_plaza" };
        this.runtime.applyTrait(portal, '@interactive');

        // Lighting
        const sun = new DirectionalLight("Sun", new Vector3(-10, -10, -5), this.scene);
        sun.position = new Vector3(10, 10, 5);
        sun.diffuse = this.hexToColor("#fff8e1");
        sun.intensity = 1.2;

        const glowOrb = new PointLight("GlowOrb", new Vector3(0, 3, 0), this.scene);
        glowOrb.diffuse = this.hexToColor("#ff00ff");
        glowOrb.intensity = 0.8;
        glowOrb.range = 10;
    }

    private hexToColor(hex: string): Color3 {
        const r = parseInt(hex.slice(1, 3), 16) / 255;
        const g = parseInt(hex.slice(3, 5), 16) / 255;
        const b = parseInt(hex.slice(5, 7), 16) / 255;
        return new Color3(r, g, b);
    }

    getScene(): Scene {
        return this.scene;
    }
}`;
}

function compileToWebGPU(source: string): string {
  return `// Generated by HoloScript Compiler v3.42.0
// Source: ${source.split('\n')[0]}

// ============================================================================
// Vertex Shader
// ============================================================================

struct VertexInput {
    @location(0) position: vec3f,
    @location(1) normal: vec3f,
    @location(2) uv: vec2f,
}

struct VertexOutput {
    @builtin(position) position: vec4f,
    @location(0) worldPosition: vec3f,
    @location(1) worldNormal: vec3f,
    @location(2) uv: vec2f,
}

struct CameraUniforms {
    viewProjectionMatrix: mat4x4f,
    cameraPosition: vec3f,
    _padding: f32,
}

struct ModelUniforms {
    modelMatrix: mat4x4f,
    normalMatrix: mat4x4f,
}

struct MaterialUniforms {
    baseColor: vec4f,
    emissiveColor: vec3f,
    emissiveIntensity: f32,
    roughness: f32,
    metallic: f32,
    _padding: vec2f,
}

@group(0) @binding(0) var<uniform> camera: CameraUniforms;
@group(1) @binding(0) var<uniform> model: ModelUniforms;
@group(2) @binding(0) var<uniform> material: MaterialUniforms;

@vertex
fn vertexMain(input: VertexInput) -> VertexOutput {
    var output: VertexOutput;

    let worldPosition = model.modelMatrix * vec4f(input.position, 1.0);
    output.worldPosition = worldPosition.xyz;
    output.position = camera.viewProjectionMatrix * worldPosition;

    let worldNormal = model.normalMatrix * vec4f(input.normal, 0.0);
    output.worldNormal = normalize(worldNormal.xyz);

    output.uv = input.uv;

    return output;
}

// ============================================================================
// Fragment Shader - PBR Lighting
// ============================================================================

const PI: f32 = 3.14159265359;

fn fresnelSchlick(cosTheta: f32, F0: vec3f) -> vec3f {
    return F0 + (1.0 - F0) * pow(1.0 - cosTheta, 5.0);
}

fn distributionGGX(N: vec3f, H: vec3f, roughness: f32) -> f32 {
    let a = roughness * roughness;
    let a2 = a * a;
    let NdotH = max(dot(N, H), 0.0);
    let NdotH2 = NdotH * NdotH;

    let num = a2;
    var denom = (NdotH2 * (a2 - 1.0) + 1.0);
    denom = PI * denom * denom;

    return num / denom;
}

fn geometrySchlickGGX(NdotV: f32, roughness: f32) -> f32 {
    let r = (roughness + 1.0);
    let k = (r * r) / 8.0;

    let num = NdotV;
    let denom = NdotV * (1.0 - k) + k;

    return num / denom;
}

fn geometrySmith(N: vec3f, V: vec3f, L: vec3f, roughness: f32) -> f32 {
    let NdotV = max(dot(N, V), 0.0);
    let NdotL = max(dot(N, L), 0.0);
    let ggx2 = geometrySchlickGGX(NdotV, roughness);
    let ggx1 = geometrySchlickGGX(NdotL, roughness);

    return ggx1 * ggx2;
}

@fragment
fn fragmentMain(input: VertexOutput) -> @location(0) vec4f {
    let albedo = material.baseColor.rgb;
    let N = normalize(input.worldNormal);
    let V = normalize(camera.cameraPosition - input.worldPosition);

    // Fresnel reflectance at normal incidence
    var F0 = vec3f(0.04);
    F0 = mix(F0, albedo, material.metallic);

    // Light direction (simple directional light)
    let L = normalize(vec3f(10.0, 10.0, 5.0));
    let H = normalize(V + L);

    let radiance = vec3f(1.0, 0.97, 0.88) * 1.2; // Sun color #fff8e1

    // Cook-Torrance BRDF
    let NDF = distributionGGX(N, H, material.roughness);
    let G = geometrySmith(N, V, L, material.roughness);
    let F = fresnelSchlick(max(dot(H, V), 0.0), F0);

    let numerator = NDF * G * F;
    let denominator = 4.0 * max(dot(N, V), 0.0) * max(dot(N, L), 0.0) + 0.0001;
    let specular = numerator / denominator;

    let kS = F;
    var kD = vec3f(1.0) - kS;
    kD *= 1.0 - material.metallic;

    let NdotL = max(dot(N, L), 0.0);
    var Lo = (kD * albedo / PI + specular) * radiance * NdotL;

    // Add emissive
    let emissive = material.emissiveColor * material.emissiveIntensity;
    var color = Lo + emissive;

    // Tone mapping
    color = color / (color + vec3f(1.0));

    // Gamma correction
    color = pow(color, vec3f(1.0 / 2.2));

    return vec4f(color, material.baseColor.a);
}

// ============================================================================
// Demo Scene Material Configurations
// ============================================================================

// Magic Cube Material:
// baseColor: vec4f(1.0, 0.0, 1.0, 1.0)  // #ff00ff
// emissiveColor: vec3f(1.0, 0.0, 1.0)
// emissiveIntensity: 0.5
// roughness: 0.3
// metallic: 0.7

// Floating Sphere Material:
// baseColor: vec4f(0.0, 1.0, 1.0, 1.0)  // #00ffff
// roughness: 0.5
// metallic: 0.0`;
}
