import { useState } from 'react';
import { Link, useNavigate } from 'react-router-dom';
import { useAuthStore } from '@/stores/authStore';

export default function LoginPage() {
  const navigate = useNavigate();
  const { login, isLoading, error, clearError } = useAuthStore();
  const [email, setEmail] = useState('');
  const [password, setPassword] = useState('');

  const handleSubmit = async (e: React.FormEvent) => {
    e.preventDefault();
    clearError();

    try {
      await login(email, password);
      navigate('/');
    } catch {
      // Error is handled by store
    }
  };

  return (
    <div className="w-full max-w-md">
      <div className="text-center mb-8">
        <h1 className="text-3xl font-bold text-oasis-text">Welcome Back</h1>
        <p className="text-oasis-text-muted mt-2">Sign in to continue to Hololand Oasis</p>
      </div>

      <form onSubmit={handleSubmit} className="space-y-4">
        {error && (
          <div className="p-3 bg-oasis-error/20 border border-oasis-error/50 rounded-lg text-sm text-oasis-error">
            {error}
          </div>
        )}

        <div>
          <label htmlFor="email" className="block text-sm font-medium text-oasis-text mb-1">
            Email
          </label>
          <input
            id="email"
            type="email"
            value={email}
            onChange={(e) => setEmail(e.target.value)}
            className="input"
            placeholder="you@example.com"
            required
          />
        </div>

        <div>
          <label htmlFor="password" className="block text-sm font-medium text-oasis-text mb-1">
            Password
          </label>
          <input
            id="password"
            type="password"
            value={password}
            onChange={(e) => setPassword(e.target.value)}
            className="input"
            placeholder="••••••••"
            required
          />
        </div>

        <div className="flex items-center justify-between text-sm">
          <label className="flex items-center gap-2">
            <input type="checkbox" className="rounded border-white/20 bg-oasis-surface-light" />
            <span className="text-oasis-text-muted">Remember me</span>
          </label>
          <Link
            to="/auth/forgot-password"
            className="text-oasis-primary hover:text-oasis-primary-light"
          >
            Forgot password?
          </Link>
        </div>

        <button type="submit" disabled={isLoading} className="btn-primary w-full">
          {isLoading ? 'Signing in...' : 'Sign In'}
        </button>
      </form>

      <div className="mt-6">
        <div className="relative">
          <div className="absolute inset-0 flex items-center">
            <div className="w-full border-t border-white/10" />
          </div>
          <div className="relative flex justify-center text-sm">
            <span className="px-2 bg-oasis-surface text-oasis-text-muted">Or continue with</span>
          </div>
        </div>

        <div className="mt-6 grid grid-cols-3 gap-3">
          <button className="btn-secondary flex items-center justify-center">
            <GoogleIcon className="w-5 h-5" />
          </button>
          <button className="btn-secondary flex items-center justify-center">
            <DiscordIcon className="w-5 h-5" />
          </button>
          <button className="btn-secondary flex items-center justify-center">
            <WalletIcon className="w-5 h-5" />
          </button>
        </div>
      </div>

      <p className="mt-8 text-center text-sm text-oasis-text-muted">
        Don't have an account?{' '}
        <Link to="/auth/register" className="text-oasis-primary hover:text-oasis-primary-light">
          Sign up
        </Link>
      </p>
    </div>
  );
}

