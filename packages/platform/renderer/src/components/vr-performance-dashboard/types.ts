/**
 * Get the theme color for a performance state.
 */
export function getPerformanceStateColor(
  state: 'nominal' | 'pressure' | 'critical' | 'emergency',
  theme: VRPerfTheme,
): string {
  switch (state) {
    case 'nominal': return theme.nominalColor;
    case 'pressure': return theme.pressureColor;
    case 'critical': return theme.criticalColor;
    case 'emergency': return theme.emergencyColor;
    default: return theme.textMuted;
  }
}

/**
 * Get the theme color for a Gaussian layer type.
 */
export function getLayerColor(layer: GaussianLayerType, theme: VRPerfTheme): string {
  return theme[LAYER_DISPLAY_CONFIG[layer].colorKey] as string;
}

/**
 * Format a splat count for display (e.g., 120000 -> "120K").
 */
export function formatSplatCount(count: number): string {
  if (count >= 1_000_000) return `${(count / 1_000_000).toFixed(1)}M`;
  if (count >= 1_000) return `${(count / 1_000).toFixed(1)}K`;
  return count.toString();
}

/**
 * Format milliseconds for display.
 */
export function formatMs(ms: number): string {
  if (ms < 1) {
    const us = ms * 1000;
    if (us < 1) return '<0.01ms';
    return `${us.toFixed(0)}us`;
  }
  if (ms < 100) return `${ms.toFixed(2)}ms`;
  return `${ms.toFixed(0)}ms`;
}

/**
 * Format bytes for display.
 */
export function formatBytes(bytes: number): string {
  if (bytes < 1024) return `${bytes}B`;
  if (bytes < 1024 * 1024) return `${(bytes / 1024).toFixed(1)}KB`;
  return `${(bytes / (1024 * 1024)).toFixed(2)}MB`;
}

/**
 * Create a unique alert ID.
 */
export function createAlertId(): string {
  return `alert-${Date.now()}-${Math.random().toString(36).substring(2, 8)}`;
}
