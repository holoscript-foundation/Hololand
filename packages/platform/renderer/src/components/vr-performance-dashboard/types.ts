/**
 * VR Performance Dashboard - Shared Types
 *
 * Type definitions for the VR performance monitoring dashboard that
 * visualizes Gaussian splat budget utilization and frame time waterfall.
 *
 * Integrates with:
 *   - GaussianBudgetManager: Layer-level budget (120K+30K+10K=160K Quest 3)
 *   - FoveatedGaussianRenderer: Per-frame render timings
 *   - QualityManager: Adaptive quality state
 *
 * Target: Quest 3 at 90Hz (11.1ms frame budget, ~5.5ms for splat rendering)
 *
 * @module vr-performance-dashboard/types
 */

import type {
  GaussianLayerType,
  GaussianBudgetMetrics,
  LayerBudgetState,
} from '../../GaussianBudgetManager';

import type {
  GaussianRenderTimings,
  GaussianRenderStats,
} from '../../FoveatedGaussianTypes';

// =============================================================================
// DASHBOARD DISPLAY MODES
// =============================================================================

/**
 * Display mode for the VR Performance Dashboard.
 */
export type VRPerfDisplayMode =
  | 'dashboard'     // Full dashboard with all panels
  | 'compact'       // Compact HUD bar for VR overlay
  | 'budget-only'   // Only Gaussian budget panel
  | 'waterfall-only' // Only frame time waterfall
  | 'overlay';      // Semi-transparent overlay for VR HUD

/**
 * Panels that can be shown/hidden in the dashboard.
 */
export type VRPerfPanel =
  | 'budget'         // Gaussian budget utilization per-layer
  | 'waterfall'      // Frame time waterfall chart
  | 'summary'        // Performance summary metrics
  | 'memory'         // VRAM usage per layer
  | 'history'        // Frame time history line chart
  | 'alerts';        // Performance alerts (emergency shed, etc.)

// =============================================================================
// PERFORMANCE STATE
// =============================================================================

/**
 * A single frame time sample with full waterfall breakdown.
 */
export interface FrameTimeSample {
  /** Frame number (monotonically increasing) */
  frameNumber: number;
  /** Timestamp when this frame was recorded */
  timestamp: number;
  /** Full render timing breakdown for this frame */
  timings: GaussianRenderTimings;
}

/**
 * Performance alert generated by the dashboard.
 */
export interface PerformanceAlert {
  /** Unique alert ID */
  id: string;
  /** Alert timestamp */
  timestamp: number;
  /** Alert severity */
  severity: 'info' | 'warning' | 'critical';
  /** Alert category */
  category: 'budget' | 'frame_time' | 'memory' | 'emergency';
  /** Human-readable message */
  message: string;
  /** Whether the alert has been dismissed */
  dismissed: boolean;
}

/**
 * Overall VR performance state consumed by the dashboard.
 */
export interface VRPerformanceState {
  /** Current Gaussian budget metrics (from GaussianBudgetManager.getMetrics()) */
  budgetMetrics: GaussianBudgetMetrics | null;
  /** Current render stats (rolling aggregates from FoveatedGaussianRenderer) */
  renderStats: GaussianRenderStats | null;
  /** Recent frame time samples for waterfall visualization */
  frameSamples: FrameTimeSample[];
  /** Target frame time in ms (11.1ms for 90Hz VR) */
  targetFrameTimeMs: number;
  /** Target Gaussian budget (e.g., 5.5ms for splat rendering) */
  targetSplatBudgetMs: number;
  /** Active performance alerts */
  alerts: PerformanceAlert[];
  /** Whether the dashboard is connected to live data */
  isLive: boolean;
  /** Device preset name */
  devicePreset: string;
  /** Current frames per second */
  currentFps: number;
}

/**
 * Actions available from the useVRPerformance hook.
 */
export interface VRPerformanceActions {
  /** Push new budget metrics (called by GaussianBudgetManager listener) */
  updateBudgetMetrics: (metrics: GaussianBudgetMetrics) => void;
  /** Push a new frame timing sample */
  pushFrameSample: (timings: GaussianRenderTimings, frameNumber: number) => void;
  /** Push updated render stats */
  updateRenderStats: (stats: GaussianRenderStats) => void;
  /** Dismiss a performance alert */
  dismissAlert: (id: string) => void;
  /** Clear all alerts */
  clearAlerts: () => void;
  /** Set target frame time */
  setTargetFrameTimeMs: (ms: number) => void;
  /** Toggle live/paused data feed */
  toggleLive: () => void;
}

