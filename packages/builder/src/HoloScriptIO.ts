/**
 * Convert parsed internal AST to a Scene object
 */
function internalAstToScene(ast: HoloScriptAST, idPrefix: string): Scene {
  const generateId = () => `${idPrefix}${Date.now().toString(36)}-${Math.random().toString(36).substr(2, 9)}`;

  const scene: Scene = {
    id: generateId(),
    name: ast.composition?.name || 'Imported Scene',
    nodes: new Map(),
    rootNodes: [],
    scripts: new Map(),
    assets: new Map(),
    settings: {
      ambientLight: { color: '#404040', intensity: 0.5 },
      physics: { gravity: { x: 0, y: -9.81, z: 0 }, enabled: true },
    },
  };

  // Apply settings from AST
  if (ast.composition?.settings) {
    const s = ast.composition.settings;
    if (s.ambient_light) {
      const al = s.ambient_light as Record<string, unknown>;
      scene.settings.ambientLight = {
        color: (al.color as string) || '#404040',
        intensity: (al.intensity as number) || 0.5,
      };
    }
    if (s.physics) {
      const p = s.physics as Record<string, unknown>;
      scene.settings.physics = {
        enabled: (p.enabled as boolean) ?? true,
        gravity: arrayToVector3((p.gravity as number[]) || [0, -9.81, 0]),
      };
    }
    if (s.skybox) {
      scene.settings.skybox = s.skybox as string;
    }
    if (s.fog) {
      const f = s.fog as Record<string, unknown>;
      scene.settings.fog = {
        color: (f.color as string) || '#ffffff',
        near: (f.near as number) || 10,
        far: (f.far as number) || 100,
      };
    }
  }

  // Convert nodes
  for (const astNode of ast.composition?.nodes || []) {
    const node = astNodeToSceneNode(astNode, null, scene, generateId);
    scene.nodes.set(node.id, node);
    scene.rootNodes.push(node.id);
  }

  // Convert logic blocks to visual scripts
  if (ast.composition?.logic) {
    const script: VisualScript = {
      id: generateId(),
      name: 'Imported Script',
      nodes: new Map(),
      connections: [],
      variables: [],
    };

    for (const logicBlock of ast.composition.logic) {
      // Create event node
      const eventNode: VisualScriptNode = {
        id: generateId(),
        type: 'event',
        position: { x: 0, y: script.nodes.size * 100 },
        inputs: [],
        outputs: [{ id: generateId(), name: 'flow', type: 'flow', connected: [] }],
        properties: { eventName: logicBlock.event },
      };
      script.nodes.set(eventNode.id, eventNode);

      // Create action nodes for each action
      let prevNodeId = eventNode.id;
      for (const action of logicBlock.actions) {
        const actionNode: VisualScriptNode = {
          id: generateId(),
          type: 'action',
          position: { x: 200, y: script.nodes.size * 100 },
          inputs: [{ id: generateId(), name: 'flow', type: 'flow', connected: [] }],
          outputs: [{ id: generateId(), name: 'flow', type: 'flow', connected: [] }],
          properties: { action: action },
        };
        script.nodes.set(actionNode.id, actionNode);

        // Connect previous node to this one
        const connection: VisualScriptConnection = {
          id: generateId(),
          from: { nodeId: prevNodeId, portId: 'flow' },
          to: { nodeId: actionNode.id, portId: 'flow' },
        };
        script.connections.push(connection);
        prevNodeId = actionNode.id;
      }
    }

    scene.scripts.set(script.id, script);
  }

  return scene;
}

/**
 * Convert an AST node to a SceneNode
 */
function astNodeToSceneNode(
  astNode: HoloScriptASTNode,
  parent: string | null,
  scene: Scene,
  generateId: () => string
): SceneNode {
  const nodeType = HOLO_TO_NODE_TYPE[astNode.type] || 'mesh';
  const id = generateId();

  // Extract transform from properties
  const position = arrayToVector3((astNode.properties.position as number[]) || [0, 0, 0]);
  const rotationArr = (astNode.properties.rotation as number[]) || [0, 0, 0];
  const rotation = eulerToQuaternion({ x: rotationArr[0], y: rotationArr[1], z: rotationArr[2] });
  const scale = arrayToVector3((astNode.properties.scale as number[]) || [1, 1, 1]);

  // Extract components from traits
  const components: ComponentInstance[] = [];
  const traits = (astNode.properties.traits as string[]) || [];
  for (const trait of traits) {
    components.push({
      id: generateId(),
      componentType: traitToComponent(trait),
      properties: {},
      enabled: true,
    });
  }

  const node: SceneNode = {
    id,
    name: astNode.name || 'Imported Node',
    type: nodeType,
    transform: { position, rotation, scale },
    parent,
    children: [],
    components,
    visible: astNode.properties.visible !== false,
    locked: astNode.properties.locked === true,
    metadata: (astNode.properties.metadata as Record<string, unknown>) || {},
  };

  // Process children
  for (const childAst of astNode.children) {
    const childNode = astNodeToSceneNode(childAst, id, scene, generateId);
    scene.nodes.set(childNode.id, childNode);
    node.children.push(childNode.id);
  }

  return node;
}

/**
 * Map HoloScript traits to component types
 */
function traitToComponent(trait: string): string {
  const componentMap: Record<string, string> = {
    collidable: 'Collider',
    interactive: 'Interactable',
    talkable: 'Talkable',
    patrol: 'Patrol',
    physics: 'Physics',
    audio: 'Audio',
    animated: 'Animation',
    particle: 'Particle',
    emissive: 'Light',
  };
  return componentMap[trait] || trait;
}

/**
 * Validate a scene for common issues
 */
function validateScene(scene: Scene): HoloScriptError[] {
  const errors: HoloScriptError[] = [];

  // Check for orphan nodes
  for (const [id, node] of scene.nodes) {
    if (node.parent && !scene.nodes.has(node.parent)) {
      errors.push({
        line: 0,
        column: 0,
        message: `Node "${node.name}" references non-existent parent`,
        severity: 'warning',
      });
    }
  }

  // Check for empty scene
  if (scene.nodes.size === 0) {
    errors.push({
      line: 0,
      column: 0,
      message: 'Scene contains no nodes',
      severity: 'warning',
    });
  }

  return errors;
}

