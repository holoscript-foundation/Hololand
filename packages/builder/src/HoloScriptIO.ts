function escapeString(s: string): string {
  return s.replace(/\\/g, '\\\\').replace(/"/g, '\\"');
}

function formatValue(value: unknown): string {
  if (value === null || value === undefined) return 'null';
  if (typeof value === 'string') return `"${escapeString(value)}"`;
  if (typeof value === 'number') return String(value);
  if (typeof value === 'boolean') return String(value);
  if (Array.isArray(value)) {
    return `[${value.map(formatValue).join(', ')}]`;
  }
  if (typeof value === 'object') {
    const entries = Object.entries(value)
      .map(([k, v]) => `${k}: ${formatValue(v)}`)
      .join(', ');
    return `{ ${entries} }`;
  }
  return String(value);
}

function formatVector3(v: Vector3): string {
  return `[${v.x}, ${v.y}, ${v.z}]`;
}

function isIdentityRotation(q: { x: number; y: number; z: number; w: number }): boolean {
  return q.x === 0 && q.y === 0 && q.z === 0 && q.w === 1;
}

function isUniformScale(s: Vector3): boolean {
  return s.x === 1 && s.y === 1 && s.z === 1;
}

function quaternionToEuler(q: { x: number; y: number; z: number; w: number }): Vector3 {
  // Simplified conversion - production would use proper math
  const sinr_cosp = 2 * (q.w * q.x + q.y * q.z);
  const cosr_cosp = 1 - 2 * (q.x * q.x + q.y * q.y);
  const roll = Math.atan2(sinr_cosp, cosr_cosp);

  const sinp = 2 * (q.w * q.y - q.z * q.x);
  const pitch = Math.abs(sinp) >= 1 ? Math.sign(sinp) * Math.PI / 2 : Math.asin(sinp);

  const siny_cosp = 2 * (q.w * q.z + q.x * q.y);
  const cosy_cosp = 1 - 2 * (q.y * q.y + q.z * q.z);
  const yaw = Math.atan2(siny_cosp, cosy_cosp);

  // Convert to degrees
  const toDeg = 180 / Math.PI;
  return { x: roll * toDeg, y: pitch * toDeg, z: yaw * toDeg };
}

function eulerToQuaternion(euler: Vector3): { x: number; y: number; z: number; w: number } {
  // Convert degrees to radians
  const toRad = Math.PI / 180;
  const roll = euler.x * toRad;
  const pitch = euler.y * toRad;
  const yaw = euler.z * toRad;

  const cr = Math.cos(roll * 0.5);
  const sr = Math.sin(roll * 0.5);
  const cp = Math.cos(pitch * 0.5);
  const sp = Math.sin(pitch * 0.5);
  const cy = Math.cos(yaw * 0.5);
  const sy = Math.sin(yaw * 0.5);

  return {
    w: cr * cp * cy + sr * sp * sy,
    x: sr * cp * cy - cr * sp * sy,
    y: cr * sp * cy + sr * cp * sy,
    z: cr * cp * sy - sr * sp * cy,
  };
}

function arrayToVector3(arr: number[]): Vector3 {
  return { x: arr[0] || 0, y: arr[1] || 0, z: arr[2] || 0 };
}

