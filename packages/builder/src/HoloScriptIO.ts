/**
 * Export a Scene to HoloScript (.holo) format
 *
 * @example
 * ```typescript
 * const holoScript = exportToHoloScript(scene, { includeComments: true });
 * fs.writeFileSync('scene.holo', holoScript);
 * ```
 */
export function exportToHoloScript(
  scene: Scene,
  options: HoloScriptExportOptions = {}
): string {
  const {
    includeComments = true,
    indent = 2,
    includeMetadata = true,
    exportScripts = true,
    exportAssets = true,
  } = options;

  const lines: string[] = [];
  const ind = (level: number) => ' '.repeat(indent * level);

  // Header comment
  if (includeComments) {
    lines.push('// ============================================================================');
    lines.push(`// ${scene.name}`);
    lines.push('// Generated by Hololand WorldBuilder');
    if (includeMetadata) {
      lines.push(`// Exported: ${new Date().toISOString()}`);
    }
    lines.push('// ============================================================================');
    lines.push('');
  }

  // Composition block
  lines.push(`composition "${escapeString(scene.name)}" {`);

  // Metadata
  if (includeMetadata) {
    lines.push('');
    lines.push(`${ind(1)}meta {`);
    lines.push(`${ind(2)}id: "${scene.id}"`);
    lines.push(`${ind(2)}version: "1.0.0"`);
    lines.push(`${ind(2)}generator: "hololand-builder"`);
    lines.push(`${ind(1)}}`);
  }

  // Scene settings
  lines.push('');
  lines.push(`${ind(1)}settings {`);
  lines.push(...exportSceneSettings(scene.settings, indent, 2));
  lines.push(`${ind(1)}}`);

  // Assets
  if (exportAssets && scene.assets.size > 0) {
    lines.push('');
    if (includeComments) {
      lines.push(`${ind(1)}// Assets`);
    }
    lines.push(`${ind(1)}assets {`);
    for (const [, asset] of scene.assets) {
      lines.push(...exportAsset(asset, indent, 2));
    }
    lines.push(`${ind(1)}}`);
  }

  // Export root nodes and their children
  lines.push('');
  if (includeComments) {
    lines.push(`${ind(1)}// Scene Graph`);
  }

  for (const rootId of scene.rootNodes) {
    const rootNode = scene.nodes.get(rootId);
    if (rootNode) {
      lines.push(...exportNode(rootNode, scene, indent, 1, includeComments));
    }
  }

  // Export visual scripts as logic blocks
  if (exportScripts && scene.scripts.size > 0) {
    lines.push('');
    if (includeComments) {
      lines.push(`${ind(1)}// Logic & Events`);
    }
    lines.push(`${ind(1)}logic {`);
    for (const [, script] of scene.scripts) {
      lines.push(...exportVisualScript(script, indent, 2, includeComments));
    }
    lines.push(`${ind(1)}}`);
  }

  lines.push('}');

  return lines.join('\n');
}

/**
 * Export scene settings to HoloScript
 */
function exportSceneSettings(settings: SceneSettings, indent: number, level: number): string[] {
  const lines: string[] = [];
  const ind = (l: number) => ' '.repeat(indent * l);

  // Ambient light
  lines.push(`${ind(level)}ambient_light {`);
  lines.push(`${ind(level + 1)}color: "${settings.ambientLight.color}"`);
  lines.push(`${ind(level + 1)}intensity: ${settings.ambientLight.intensity}`);
  lines.push(`${ind(level)}}`);

  // Physics
  lines.push(`${ind(level)}physics {`);
  lines.push(`${ind(level + 1)}enabled: ${settings.physics.enabled}`);
  lines.push(`${ind(level + 1)}gravity: [${settings.physics.gravity.x}, ${settings.physics.gravity.y}, ${settings.physics.gravity.z}]`);
  lines.push(`${ind(level)}}`);

  // Skybox
  if (settings.skybox) {
    lines.push(`${ind(level)}skybox: "${settings.skybox}"`);
  }

  // Fog
  if (settings.fog) {
    lines.push(`${ind(level)}fog {`);
    lines.push(`${ind(level + 1)}color: "${settings.fog.color}"`);
    lines.push(`${ind(level + 1)}near: ${settings.fog.near}`);
    lines.push(`${ind(level + 1)}far: ${settings.fog.far}`);
    lines.push(`${ind(level)}}`);
  }

  return lines;
}

/**
 * Export an asset reference
 */
function exportAsset(asset: AssetReference, indent: number, level: number): string[] {
  const lines: string[] = [];
  const ind = (l: number) => ' '.repeat(indent * l);

  lines.push(`${ind(level)}${asset.type} "${escapeString(asset.name)}" {`);
  lines.push(`${ind(level + 1)}path: "${asset.path}"`);
  if (asset.thumbnail) {
    lines.push(`${ind(level + 1)}thumbnail: "${asset.thumbnail}"`);
  }
  for (const [key, value] of Object.entries(asset.metadata)) {
    lines.push(`${ind(level + 1)}${key}: ${formatValue(value)}`);
  }
  lines.push(`${ind(level)}}`);

  return lines;
}

/**
 * Export a scene node and its children recursively
 */
function exportNode(
  node: SceneNode,
  scene: Scene,
  indent: number,
  level: number,
  includeComments: boolean
): string[] {
  const lines: string[] = [];
  const ind = (l: number) => ' '.repeat(indent * l);
  const holoType = NODE_TYPE_TO_HOLO[node.type] || 'object';

  // Determine if this is a group/container or a leaf node
  const isContainer = node.type === 'group' || node.children.length > 0;

  lines.push('');

  if (isContainer) {
    // Use spatial_group for containers
    lines.push(`${ind(level)}spatial_group "${escapeString(node.name)}" {`);
  } else {
    // Use object/specific type for leaf nodes
    lines.push(`${ind(level)}${holoType} "${escapeString(node.name)}" {`);
  }

  // Transform
  lines.push(`${ind(level + 1)}position: ${formatVector3(node.transform.position)}`);

  // Only include non-identity rotation
  if (!isIdentityRotation(node.transform.rotation)) {
    const euler = quaternionToEuler(node.transform.rotation);
    lines.push(`${ind(level + 1)}rotation: ${formatVector3(euler)}`);
  }

  // Only include non-uniform scale
  if (!isUniformScale(node.transform.scale)) {
    lines.push(`${ind(level + 1)}scale: ${formatVector3(node.transform.scale)}`);
  }

  // Visibility
  if (!node.visible) {
    lines.push(`${ind(level + 1)}visible: false`);
  }

  // Locked state
  if (node.locked) {
    lines.push(`${ind(level + 1)}locked: true`);
  }

  // Components as traits or properties
  if (node.components.length > 0) {
    lines.push('');
    if (includeComments) {
      lines.push(`${ind(level + 1)}// Components`);
    }
    lines.push(...exportComponents(node.components, indent, level + 1));
  }

  // Metadata
  if (Object.keys(node.metadata).length > 0) {
    lines.push('');
    lines.push(`${ind(level + 1)}metadata {`);
    for (const [key, value] of Object.entries(node.metadata)) {
      lines.push(`${ind(level + 2)}${key}: ${formatValue(value)}`);
    }
    lines.push(`${ind(level + 1)}}`);
  }

  // Child nodes
  for (const childId of node.children) {
    const child = scene.nodes.get(childId);
    if (child) {
      lines.push(...exportNode(child, scene, indent, level + 1, includeComments));
    }
  }

  lines.push(`${ind(level)}}`);

  return lines;
}

/**
 * Export components as HoloScript traits and properties
 */
function exportComponents(components: ComponentInstance[], indent: number, level: number): string[] {
  const lines: string[] = [];
  const ind = (l: number) => ' '.repeat(indent * l);

  // Group components by type for traits
  const traits: string[] = [];

  for (const component of components) {
    if (!component.enabled) continue;

    // Some components map directly to traits
    const traitName = componentToTrait(component.componentType);
    if (traitName) {
      traits.push(traitName);
    }

    // Export component properties
    if (Object.keys(component.properties).length > 0) {
      lines.push(`${ind(level)}${component.componentType} {`);
      for (const [key, value] of Object.entries(component.properties)) {
        lines.push(`${ind(level + 1)}${key}: ${formatValue(value)}`);
      }
      lines.push(`${ind(level)}}`);
    }
  }

  // Add traits line if any
  if (traits.length > 0) {
    lines.unshift(`${ind(level)}traits: [${traits.map(t => `"${t}"`).join(', ')}]`);
  }

  return lines;
}

/**
 * Map component types to HoloScript traits
 */
function componentToTrait(componentType: string): string | null {
  const traitMap: Record<string, string> = {
    Collider: 'collidable',
    Interactable: 'interactive',
    Talkable: 'talkable',
    Patrol: 'patrol',
    Physics: 'physics',
    Audio: 'audio',
    Animation: 'animated',
    Particle: 'particle',
    Light: 'emissive',
  };
  return traitMap[componentType] || null;
}

/**
 * Export a visual script to HoloScript logic block
 */
function exportVisualScript(
  script: VisualScript,
  indent: number,
  level: number,
  includeComments: boolean
): string[] {
  const lines: string[] = [];
  const ind = (l: number) => ' '.repeat(indent * l);

  if (includeComments) {
    lines.push(`${ind(level)}// Script: ${script.name}`);
  }

  // Export variables as state
  if (script.variables.length > 0) {
    lines.push(`${ind(level)}state {`);
    for (const variable of script.variables) {
      const value = formatValue(variable.defaultValue);
      lines.push(`${ind(level + 1)}${variable.name}: ${value}`);
    }
    lines.push(`${ind(level)}}`);
    lines.push('');
  }

  // Find event nodes and trace their execution paths
  const eventNodes = Array.from(script.nodes.values()).filter(n => n.type === 'event');

  for (const eventNode of eventNodes) {
    const eventName = (eventNode.properties.eventName as string) || 'on_event';
    lines.push(`${ind(level)}${eventName} {`);

    // Trace the execution flow from this event
    const actions = traceExecutionFlow(eventNode, script);
    for (const action of actions) {
      lines.push(`${ind(level + 1)}${action}`);
    }

    lines.push(`${ind(level)}}`);
    lines.push('');
  }

  return lines;
}

/**
 * Trace execution flow from an event node through connected action nodes
 */
function traceExecutionFlow(eventNode: VisualScriptNode, script: VisualScript): string[] {
  const actions: string[] = [];
  const visited = new Set<string>();
  const queue: string[] = [];

  // Find flow outputs from event node
  const flowOutputs = eventNode.outputs.filter(p => p.type === 'flow');
  for (const output of flowOutputs) {
    for (const connectedId of output.connected) {
      queue.push(connectedId);
    }
  }

  // BFS through connected nodes
  while (queue.length > 0) {
    const connectionId = queue.shift()!;
    if (visited.has(connectionId)) continue;
    visited.add(connectionId);

    // Find the connection
    const connection = script.connections.find(c => c.id === connectionId);
    if (!connection) continue;

    // Get the target node
    const targetNode = script.nodes.get(connection.to.nodeId);
    if (!targetNode) continue;

    // Convert node to HoloScript action
    const action = nodeToHoloScriptAction(targetNode);
    if (action) {
      actions.push(action);
    }

    // Continue tracing flow
    const nextFlowOutputs = targetNode.outputs.filter(p => p.type === 'flow');
    for (const output of nextFlowOutputs) {
      for (const nextConnectedId of output.connected) {
        queue.push(nextConnectedId);
      }
    }
  }

  return actions;
}

/**
 * Convert a visual script node to a HoloScript action statement
 */
function nodeToHoloScriptAction(node: VisualScriptNode): string | null {
  const props = node.properties;

  switch (node.type) {
    case 'action':
      const actionName = (props.action as string) || 'do_action';
      const target = (props.target as string) || 'self';
      const params = (props.params as Record<string, unknown>) || {};
      const paramStr = Object.entries(params)
        .map(([k, v]) => `${k}: ${formatValue(v)}`)
        .join(', ');
      return paramStr ? `${target}.${actionName}(${paramStr})` : `${target}.${actionName}()`;

    case 'condition':
      const condition = (props.condition as string) || 'true';
      return `if (${condition}) { /* ... */ }`;

    case 'variable':
      const varName = (props.name as string) || 'value';
      const varValue = formatValue(props.value);
      return `${varName} = ${varValue}`;

    case 'flow':
      const flowType = (props.flowType as string) || 'sequence';
      return `// ${flowType} flow`;

    default:
      return null;
  }
}

