function bracesBalanced(source) {
  let depth = 0;
  for (const char of source) {
    if (char === '{') depth += 1;
    else if (char === '}') depth -= 1;
    if (depth < 0) return false;
  }
  return depth === 0;
}

function structuralValidation(source, sourcePath, requiredTokens) {
  const errors = [];
  if (!bracesBalanced(source)) errors.push('curly braces are not balanced');
  for (const token of requiredTokens) {
    if (!source.includes(token)) errors.push(`missing token: ${token}`);
  }
  return {
    tool: 'hololand.describe-proof-loop.structural-guard',
    file: relativeToRepo(sourcePath),
    status: errors.length ? 'fail' : 'pass',
    errors,
  };
}

function parseWithHoloScriptCli(filePath) {
  if (!existsSync(HS_CLI)) {
    return {
      tool: 'HoloScript CLI parse',
      status: 'blocked',
      file: relativeToRepo(filePath),
      command: `node ${HS_CLI} parse ${filePath}`,
      reason: 'HoloScript CLI dist entry not found',
    };
  }

  const result = spawnSync(process.execPath, [HS_CLI, 'parse', filePath], {
    cwd: path.dirname(HS_CLI),
    encoding: 'utf8',
    windowsHide: true,
    timeout: 60_000,
  });
  return {
    tool: 'HoloScript CLI parse',
    status: result.status === 0 ? 'pass' : 'fail',
    file: relativeToRepo(filePath),
    command: `node ${HS_CLI} parse ${filePath}`,
    exitCode: result.status,
    stdoutTail: String(result.stdout || '').split(/\r?\n/).filter(Boolean).slice(-5),
    stderrTail: String(result.stderr || '').split(/\r?\n/).filter(Boolean).slice(-5),
  };
}

function writeJson(filePath, value) {
  mkdirSync(path.dirname(filePath), { recursive: true });
  writeFileSync(filePath, `${JSON.stringify(value, null, 2)}\n`, 'utf8');
}

function writeText(filePath, value) {
  mkdirSync(path.dirname(filePath), { recursive: true });
  writeFileSync(filePath, value, 'utf8');
}

function buildLearningSignals(receipt) {
  const base = {
    schema: 'hololand.learning-signal.v0.1.0',
    sourceReceipt: receipt.receipt.output,
    sourceHash: receipt.generatedWorld.sha256,
    generatedAt: receipt.generatedAt,
  };
  return [
    {
      ...base,
      type: 'pattern',
      label: 'describe_to_hololand_proof_loop',
      input: receipt.description.text,
      output: receipt.generatedWorld.path,
      evidence: receipt.validation.status,
    },
    {
      ...base,
      type: 'decision',
      label: 'source_precedes_projection',
      content: 'HoloLand render/register surfaces are projection-only until HoloScript source parses.',
      evidence: receipt.registration.path,
    },
    {
      ...base,
      type: 'next_action',
      label: 'cold_agent_continue',
      command: receipt.agentTrace.firstNextCommand,
      evidence: receipt.render.html,
    },
  ];
}

