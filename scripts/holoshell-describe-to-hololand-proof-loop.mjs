function run(args) {
  if (!existsSync(args.source)) throw new Error(`Source file not found: ${args.source}`);
  const contractSource = readFileSync(args.source, 'utf8');
  const worldSource = generatedWorldSource(args.description);
  writeText(args.world, worldSource);

  const contractStructural = structuralValidation(contractSource, args.source, [
    'composition "HoloShell Describe To HoloLand Proof Loop"',
    'policy "DescriptionCreatesSource"',
    'policy "ValidateBeforeRegistration"',
    'policy "HoloLandProjectionOnly"',
    'policy "AgentActionLeavesTrace"',
    'policy "LearningSignalIsStructured"',
    'template "DescribeToHoloLandReceipt"',
    'action export_learning_signal',
  ]);
  const worldStructural = structuralValidation(worldSource, args.world, [
    'composition "Generated HoloLand Proof Room"',
    'object "IntentPanel"',
    'object "ValidationPanel"',
    'object "AgentTraceToken"',
    'receiptRequired: true',
  ]);
  const contractCli = parseWithHoloScriptCli(args.source);
  const worldCli = parseWithHoloScriptCli(args.world);
  const validationStatus = [
    contractStructural.status,
    worldStructural.status,
    contractCli.status,
    worldCli.status,
  ].every((status) => status === 'pass') ? 'pass' : 'fail';

  const sourceHash = sha256(contractSource);
  const worldHash = sha256(worldSource);
  const registry = {
    schema: 'hololand.holoshell.describe-to-hololand-registry.v0.1.0',
    status: validationStatus === 'pass' ? 'registered' : 'blocked',
    generatedAt: new Date().toISOString(),
    sourceLayer: 'HoloScript',
    projectionOnly: true,
    description: args.description,
    contractSource: relativeToRepo(args.source),
    generatedWorldSource: relativeToRepo(args.world),
    generatedWorldSha256: worldHash,
    renderOutput: relativeToRepo(args.html),
  };
  writeJson(args.registry, registry);

  const receipt = {
    schema: SCHEMA,
    status: validationStatus,
    generatedAt: new Date().toISOString(),
    description: {
      text: args.description,
      sha256: sha256(args.description),
    },
    contractSource: {
      path: relativeToRepo(args.source),
      sha256: sourceHash,
      format: 'hsplus',
    },
    generatedWorld: {
      path: relativeToRepo(args.world),
      sha256: worldHash,
      format: 'holo',
      excerpt: worldSource.split(/\r?\n/).slice(0, 80).join('\n').trim(),
    },
    validation: {
      status: validationStatus,
      contractSource: {
        status: contractStructural.status === 'pass' && contractCli.status === 'pass' ? 'pass' : 'fail',
        structural: contractStructural,
        cli: contractCli,
      },
      generatedWorld: {
        status: worldStructural.status === 'pass' && worldCli.status === 'pass' ? 'pass' : 'fail',
        structural: worldStructural,
        cli: worldCli,
      },
    },
    registration: {
      status: registry.status,
      path: relativeToRepo(args.registry),
      projectionOnly: true,
    },
    render: {
      status: validationStatus === 'pass' ? 'ready' : 'blocked',
      html: relativeToRepo(args.html),
      url: pathToFileURL(args.html).href,
    },
    agentTrace: {
      status: validationStatus === 'pass' ? 'recorded' : 'blocked',
      action: 'registered generated HoloScript world into HoloLand proof receipt',
      firstNextCommand: `node scripts/holoshell-describe-to-hololand-proof-loop.mjs --description ${JSON.stringify(args.description)}`,
      receiptPath: relativeToRepo(args.receipt),
    },
    learningSignal: {
      status: validationStatus === 'pass' ? 'ready' : 'blocked',
      path: relativeToRepo(args.learning),
      labels: ['describe_to_hololand_proof_loop', 'source_precedes_projection', 'cold_agent_continue'],
      corpusCandidate: true,
    },
    commands: {
      run: 'node scripts/holoshell-describe-to-hololand-proof-loop.mjs',
      test: 'node scripts/__tests__/holoshell-describe-to-hololand-proof-loop.test.mjs',
    },
    receipt: {
      output: relativeToRepo(args.receipt),
      rawSecretsIncluded: false,
    },
  };
  receipt.receipt.sha256 = sha256(JSON.stringify({
    description: receipt.description.sha256,
    source: receipt.contractSource.sha256,
    generated: receipt.generatedWorld.sha256,
    validation: receipt.validation.status,
    registration: receipt.registration.status,
  }));

  const signals = buildLearningSignals(receipt);
  receipt.learningSignal.rowCount = signals.length;
  writeText(args.learning, `${signals.map((entry) => JSON.stringify(entry)).join('\n')}\n`);
  writeText(args.html, renderHtml(receipt));
  writeJson(args.receipt, receipt);

  return receipt;
}

function main() {
  try {
    const args = parseArgs();
    if (args.help) {
      console.log(usage());
      return;
    }
    const receipt = run(args);
    if (args.json) console.log(JSON.stringify(receipt, null, 2));
    else {
      console.log(`DescribeToHoloLandProofLoop: ${receipt.status}`);
      console.log(`receipt: ${receipt.receipt.output}`);
      console.log(`generated world: ${receipt.generatedWorld.path}`);
      console.log(`render: ${receipt.render.html}`);
      console.log(`learning signal: ${receipt.learningSignal.path}`);
    }
    process.exit(receipt.status === 'pass' ? 0 : 1);
  } catch (error) {
    console.error(`[describe-to-hololand-proof-loop] ${error.message}`);
    process.exit(1);
  }
}

main();
