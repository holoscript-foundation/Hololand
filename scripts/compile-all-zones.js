#!/usr/bin/env node

/**
 * Batch Compilation Script for All HoloScript Zones
 *
 * This script finds all .holo files in the repository and generates
 * cross-platform compilation examples for Unity, Unreal, Godot, Babylon, and WebGPU.
 *
 * Usage:
 *   node scripts/compile-all-zones.js [--targets unity,unreal,godot,babylon,webgpu]
 */

import { readFileSync, writeFileSync, mkdirSync, readdirSync, statSync } from 'fs';
import { join, dirname, basename, relative } from 'path';
import { glob } from 'glob';

// Configuration
const TARGETS = ['unity', 'unreal', 'godot', 'babylon', 'webgpu'];
const OUTPUT_DIR = 'examples/compiled-outputs';
const REPO_ROOT = process.cwd();

// Command line arguments
const args = process.argv.slice(2);
const targetFlag = args.find(arg => arg.startsWith('--targets='));
const selectedTargets = targetFlag
  ? targetFlag.split('=')[1].split(',')
  : TARGETS;

console.log('🚀 HoloScript Batch Compilation Tool');
console.log('=====================================');
console.log(`Targets: ${selectedTargets.join(', ')}`);
console.log(`Output: ${OUTPUT_DIR}`);
console.log('');

/**
 * Find all .holo files in the repository
 */
async function findHoloFiles() {
  console.log('📂 Searching for .holo files...');

  const files = await glob('**/*.holo', {
    cwd: REPO_ROOT,
    ignore: ['**/node_modules/**', '**/dist/**', '**/.git/**', '**/compiled-outputs/**'],
  });

  console.log(`✅ Found ${files.length} .holo files\n`);
  return files;
}

/**
 * Generate compilation note for a zone
 */
