/**
 * Main execution
 */
async function main() {
  try {
    const holoFiles = await findHoloFiles();

    if (holoFiles.length === 0) {
      console.log('❌ No .holo files found');
      return;
    }

    const compiledZones = [];
    let successCount = 0;
    let failCount = 0;

    for (let i = 0; i < holoFiles.length; i++) {
      const holoFile = holoFiles[i];
      const zoneName = basename(holoFile, '.holo');

      process.stdout.write(`[${i + 1}/${holoFiles.length}] Compiling ${zoneName}... `);

      try {
        const results = compileZone(holoFile);
        compiledZones.push({
          name: zoneName,
          source: holoFile,
          targets: results,
        });
        console.log(`✅ ${results.length} targets`);
        successCount++;
      } catch (error) {
        console.log(`❌ FAILED`);
        console.error(`   Error: ${error.message}`);
        failCount++;
      }
    }

    console.log('');
    console.log('📝 Generating summary...');
    generateSummaryREADME(compiledZones);

    console.log('');
    console.log('=====================================');
    console.log('✅ Batch Compilation Complete');
    console.log('=====================================');
    console.log(`Successful: ${successCount}`);
    console.log(`Failed: ${failCount}`);
    console.log(`Targets per zone: ${selectedTargets.length}`);
    console.log(`Total files generated: ${successCount * selectedTargets.length}`);
    console.log(`Output directory: ${OUTPUT_DIR}`);
    console.log('');
    console.log('📖 See SUMMARY.md for details');
  } catch (error) {
    console.error('❌ Fatal error:', error);
    process.exit(1);
  }
}

main();
